/**
 * 一键演示脚本 - demo-all-in-one.js
 *
 * 解决问题：
 *   每次运行 --network hardhat 都是全新的内存链，之前部署的合约不存在。
 *   本脚本在同一次运行中完成：部署合约 + 准备演示数据
 *
 * 使用方法：
 *   本地网络 (推荐演示用): npx hardhat run scripts/demo-all-in-one.js --network hardhat
 *   Fuji测试网:            npx hardhat run scripts/demo-all-in-one.js --network fuji
 */

const hre = require("hardhat");
const fs = require("fs");
const path = require("path");

async function main() {
	console.log("🚀 HerRise 一键演示数据准备脚本");
	console.log("=".repeat(50));

	// ============================================================
	// 第一步：部署合约
	// ============================================================
	console.log("\n📜 第一步：部署智能合约...");

	const [deployer, alice, bob, carol, dave] = await hre.ethers.getSigners();
	console.log(`部署账户：${deployer.address}`);

	// 部署 HerRiseToken
	const HerRiseToken = await hre.ethers.getContractFactory("HerRiseToken");
	const token = await HerRiseToken.deploy();
	await token.waitForDeployment();
	const tokenAddress = await token.getAddress();
	console.log(`✅ HerRiseToken 已部署：${tokenAddress}`);

	// 部署 HerRiseMain
	const HerRiseMain = await hre.ethers.getContractFactory("HerRiseMain");
	const main = await HerRiseMain.deploy(tokenAddress);
	await main.waitForDeployment();
	const mainAddress = await main.getAddress();
	console.log(`✅ HerRiseMain 已部署：${mainAddress}`);

	// 设置 HerRiseMain 为 minter
	await (await token.setMinter(mainAddress, true)).wait();
	console.log(`✅ HerRiseMain 已被授予铸币权限`);

	// 初始化3个学习任务
	await (await main.addTask("什么是分散投资？", "学习如何通过分散投资降低风险", hre.ethers.parseEther("10"))).wait();
	await (await main.addTask("理解风险与收益", "学习风险和收益的关系", hre.ethers.parseEther("10"))).wait();
	await (await main.addTask("什么是DeFi？", "了解去中心化金融的基本概念", hre.ethers.parseEther("15"))).wait();
	console.log(`✅ 3个学习任务已初始化`);

	// 保存合约地址到配置文件（供前端使用）
	const network = await hre.ethers.provider.getNetwork();
	const config = {
		network: hre.network.name,
		chainId: network.chainId.toString(),
		contracts: {
			HerRiseToken: tokenAddress,
			HerRiseMain: mainAddress,
		},
		deployer: deployer.address,
		deployedAt: new Date().toISOString(),
	};

	const contractsDir = path.join(__dirname, "..");
	fs.writeFileSync(
		path.join(contractsDir, "deployed-contracts.json"),
		JSON.stringify(config, null, 2)
	);

	const frontendConfigPath = path.join(contractsDir, "..", "frontend", "src", "contracts-config.json");
	fs.writeFileSync(frontendConfigPath, JSON.stringify(config, null, 2));

	const frontendRpcUrl = hre.network.name === 'fuji'
		? process.env.FUJI_RPC_URL || 'https://api.avax-test.network/ext/bc/C/rpc'
		: 'http://127.0.0.1:8545';

	const frontendEnvPath = path.join(contractsDir, "..", "frontend", ".env");
	fs.writeFileSync(
		frontendEnvPath,
		`# Auto-generated by demo-all-in-one.js on ${new Date().toISOString()}\n` +
		`VITE_TOKEN_CONTRACT_ADDRESS=${tokenAddress}\n` +
		`VITE_MAIN_CONTRACT_ADDRESS=${mainAddress}\n` +
		`VITE_CHAIN_ID=${network.chainId.toString()}\n` +
		`VITE_RPC_URL=${frontendRpcUrl}\n`
	);
	console.log(`✅ 合约地址已写入前端配置文件`);

	// ============================================================
	// 第二步：为演示账户领取水龙头代币
	// ============================================================
	console.log("\n💰 第二步：为演示账户领取水龙头代币（1000 HRT）...");

	const demoAccounts = [deployer, alice, bob, carol, dave].filter(Boolean);
	const labels = ["部署者/Admin", "Alice", "Bob", "Carol", "Dave"];

	for (let i = 0; i < demoAccounts.length; i++) {
		const account = demoAccounts[i];
		await (await token.connect(account).faucet()).wait();
		const bal = await token.balanceOf(account.address);
		console.log(`  ✅ ${labels[i]}（${account.address.slice(0, 8)}...）领取成功，余额：${hre.ethers.formatEther(bal)} HRT`);
	}

	// ============================================================
	// 第三步：创建3个不同策略的演示池
	// ============================================================
	console.log("\n🏊 第三步：创建演示理财学习池...");

	const poolsConfig = [
		{
			creator: deployer,
			name: "稳健成长池",
			strategy: "低风险稳健型 - 专注于稳定收益资产，适合初学者",
			maxMembers: 5,
			minDeposit: "50",
		},
		{
			creator: alice || deployer,
			name: "科技成长池",
			strategy: "中等风险成长型 - 投资区块链和科技领域，潜力较大",
			maxMembers: 8,
			minDeposit: "100",
		},
		{
			creator: bob || deployer,
			name: "女性领袖池",
			strategy: "高成长潜力 - 专注于女性创业企业和ESG投资",
			maxMembers: 10,
			minDeposit: "100",
		},
	];

	for (const poolCfg of poolsConfig) {
		const tx = await main
			.connect(poolCfg.creator)
			.createPool(
				poolCfg.name,
				poolCfg.strategy,
				poolCfg.maxMembers,
				hre.ethers.parseEther(poolCfg.minDeposit)
			);
		await tx.wait();
		const poolId = Number(await main.poolCount());
		console.log(`  ✅ 池 #${poolId}「${poolCfg.name}」已创建（创建者：${poolCfg.creator.address.slice(0, 8)}...，最小存款：${poolCfg.minDeposit} HRT）`);
	}

	// ============================================================
	// 第四步：多账户加入不同的池
	// ============================================================
	console.log("\n👥 第四步：演示账户加入池...");

	const joinActions = [
		// [账户, 池ID, 存款HRT]
		[deployer, 1, "100"],   // 显式让部署者加入池#1，防止分红时池子为空
		[alice, 1, "100"],      // Alice 加入稳健池 (本地模拟用)
		[bob, 1, "150"],        // Bob 加入稳健池 (本地模拟用)
		[carol, 2, "100"],      // Carol 加入科技池 (本地模拟用)
		[carol, 3, "100"],      // Carol 也加入女性领袖池 (本地模拟用)
		[dave, 3, "200"],       // Dave 加入女性领袖池 (本地模拟用)
		[deployer, 2, "200"],   // Admin 加入科技池
	].filter(([acc]) => acc);

	for (const [account, poolId, amountHRT] of joinActions) {
		const amountWei = hre.ethers.parseEther(amountHRT);
		const label = labels[demoAccounts.indexOf(account)];

		// 检查是否是创建者（创建者已在池中）
		const memberInfo = await main.getUserPoolInfo(poolId, account.address);
		if (memberInfo.exists) {
			// 创建者：直接存款
			await (await token.connect(account).approve(mainAddress, amountWei)).wait();
			await (await main.connect(account).deposit(poolId, amountWei)).wait();
			console.log(`  ✅ ${label} 向池 #${poolId} 追加存款 ${amountHRT} HRT`);
		} else {
			// 非创建者：approve + joinPool
			await (await token.connect(account).approve(mainAddress, amountWei)).wait();
			await (await main.connect(account).joinPool(poolId, amountWei)).wait();
			console.log(`  ✅ ${label} 加入池 #${poolId}，存款 ${amountHRT} HRT`);
		}
	}

	// ============================================================
	// 第五步：Alice 完成一个学习任务（展示部分完成状态）
	// ============================================================
	console.log("\n📚 第五步：Alice 完成学习任务1以展示已完成状态...");
	const taskUser = alice || deployer;
	await (await main.connect(taskUser).completeTask(1)).wait();
	const userRep = await main.reputationScore(taskUser.address);
	console.log(`  ✅ ${taskUser === deployer ? '部署者' : 'Alice'} 完成了「什么是分散投资？」，获得10 HRT奖励，信誉分：${userRep}`);

	// ============================================================
	// 第六步：模拟池1的收益分配
	// ============================================================
	console.log("\n💹 第六步：模拟池 #1 的收益分配（50 HRT）...");
	const pool1 = await main.getPoolInfo(1);
	console.log(`  池 #1 总存款：${hre.ethers.formatEther(pool1.totalDeposits)} HRT，成员数：${pool1.memberCount}`);
	await (await main.connect(deployer).distributeProfit(1, hre.ethers.parseEther("50"))).wait();
	console.log(`  ✅ 已向池 #1 分配50 HRT收益（按各成员份额比例）`);

	// ============================================================
	// 最终汇总
	// ============================================================
	console.log("\n" + "=".repeat(50));
	console.log("📊 演示数据汇总");
	console.log("=".repeat(50));

	const allPools = await main.getAllPools();
	console.log(`\n池列表（共 ${allPools.length} 个）：`);
	for (const p of allPools) {
		console.log(`  池 #${p.id}「${p.name}」`);
		console.log(`    成员：${p.memberCount}/${p.maxMembers}，总存款：${hre.ethers.formatEther(p.totalDeposits)} HRT`);
	}

	console.log("\n账户状态：");
	for (let i = 0; i < demoAccounts.length; i++) {
		const acc = demoAccounts[i];
		const bal = await token.balanceOf(acc.address);
		const [totalInvested, totalProfit, tasksCompleted, repScore, poolsJoined] =
			await main.getUserStats(acc.address);
		console.log(`  ${labels[i]}：${hre.ethers.formatEther(bal)} HRT 余额 | 投资：${hre.ethers.formatEther(totalInvested)} | 收益：${hre.ethers.formatEther(totalProfit)} | 任务：${tasksCompleted} | 信誉：${repScore}`);
	}

	console.log("\n✅ 演示环境准备完毕！");
	console.log("\n▶ 下一步：");
	console.log("  1. 在 frontend/.env 中已自动更新合约地址");
	console.log("  2. 运行前端：cd frontend && npm run dev");
	console.log(`  3. MetaMask 网络设置：\n     RPC: http://127.0.0.1:8545\n     链ID: ${network.chainId}`);
	console.log("  4. 导入演示账户私钥（Hardhat默认账户#1）：");
	console.log("     0xac0974bec39a17e36ba4a6b4d238ff944bacb478cbed5efcae784d7bf4f2ff80");
	console.log("\n📖 演示流程请参考：DEMO-SCRIPT.md");
}

main()
	.then(() => process.exit(0))
	.catch((error) => {
		console.error("\n❌ 脚本执行失败：", error.message);
		process.exit(1);
	});
